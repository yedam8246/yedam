package model;

public class Users {
	
	private String id;
	private String pw;
	private String nick;
	private int hiScore;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getNick() {
		return nick;
	}
	public void setNick(String nick) {
		this.nick = nick;
	}
	public int getHiScore() {
		return hiScore;
	}
	public void setHiScore(int hiScore) {
		this.hiScore = hiScore;
	}
	
	

}
